#!/bin/bash

echo "=========================================="
echo "停止图片管理系统"
echo "=========================================="

# 停止前端（通常通过Ctrl+C已经停止）
echo "停止前端服务..."
pkill -f "vite"

# 停止后端
echo "停止后端服务..."
pkill -f "spring-boot:run"
# 或者根据端口停止
if lsof -t -i:8080 > /dev/null 2>&1; then
    kill $(lsof -t -i:8080)
    echo "✓ 后端服务已停止"
fi

# 停止数据库
echo "停止数据库服务..."
docker compose down

echo "=========================================="
echo "所有服务已停止"
echo "=========================================="

